/*
Вам дана матрица, представляющая шахматную доску.
На ней расположены только 3 фигуры - каждая из них - конь.
Фигуры отмечены английской буквой "x", все остальные клетки доски пустые.

Конь ходит "буквой Г", то есть на 2 клетки в одну сторону и потом на 1 в другую.

Для каждого найденного на доске коня выведите все его возможные ходы.

Учтите, ваша программа должна корректно работать, даже если местоположение коней на доске изменится.

Учтите, что конь может стоять у края доски и часть ходов может быть невозможна.
Например, конь "h1" не может пойти вверх/вправо, а только вниз или влево, для него
возможные ходы только "g3" или "f2"

Правильный вывод программы должен быть следующим (порядок вывода ходов не важен):
Конь на позиции h1 может пойти на: g3, f2
Конь на позиции c2 может пойти на: e1, e3, d4, b4, a3, a1
Конь на позиции e5 может пойти на: d3, f3, g4, g6, f7, d7, c6, c4
 */

const size = 8;
const letters = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h'];

const chessboard = [
  // a  b   c   d   e   f   g   h
  ['', '', '', '', '', '', '', 'x'], // 1
  ['', '', 'x', '', '', '', '', ''], // 2
  ['', '', '', '', '', '', '', ''], // 3
  ['', '', '', '', '', '', '', ''], // 4
  ['', '', '', '', 'x', '', '', ''], // 5
  ['', '', '', '', '', '', '', ''], // 6
  ['', '', '', '', '', '', '', ''], // 7
  ['', '', '', '', '', '', '', ''], // 8
];

for (let i = 0; i < size; i++) {
  for (let j = 0; j < size; j++) {
    if (chessboard[i][j] !== 'x') {
      continue;
    }

    const currentPosition = `${letters[j]}${i + 1}`;
    const possibleMoves = [];

    // Проверка всех 8 возможных ходов коня
    // 1. Вверх-влево (2 вверх, 1 влево)
    if (i > 1 && j > 0) {
      possibleMoves.push(`${letters[j - 1]}${i - 1}`);
    }

    // 2. Вверх-вправо (2 вверх, 1 вправо)
    if (i > 1 && j < size - 1) {
      possibleMoves.push(`${letters[j + 1]}${i - 1}`);
    }

    // 3. Вправо-вверх (2 вправо, 1 вверх)
    if (j < size - 2 && i > 0) {
      possibleMoves.push(`${letters[j + 2]}${i}`);
    }

    // 4. Вправо-вниз (2 вправо, 1 вниз)
    if (j < size - 2 && i < size - 1) {
      possibleMoves.push(`${letters[j + 2]}${i + 2}`);
    }

    // 5. Вниз-вправо (2 вниз, 1 вправо)
    if (i < size - 2 && j < size - 1) {
      possibleMoves.push(`${letters[j + 1]}${i + 3}`);
    }

    // 6. Вниз-влево (2 вниз, 1 влево)
    if (i < size - 2 && j > 0) {
      possibleMoves.push(`${letters[j - 1]}${i + 3}`);
    }

    // 7. Влево-вниз (2 влево, 1 вниз)
    if (j > 1 && i < size - 1) {
      possibleMoves.push(`${letters[j - 2]}${i + 2}`);
    }

    // 8. Влево-вверх (2 влево, 1 вверх)
    if (j > 1 && i > 0) {
      possibleMoves.push(`${letters[j - 2]}${i}`);
    }

    console.log(`Конь на позиции ${currentPosition} может пойти на: ${possibleMoves.join(', ')}`);
  }
}
